#include<stdio.h>
#include<math.h>

void main() {
    
float n1 , n2;
printf("Enter two numbers:- ");
scanf("%f %f",&n1,&n2);

printf("Ceil : %f\n",ceil(n1));  
printf("floor : %f\n",floor(n1)); 
printf("sqrt : %f\n",sqrt(n1));   
printf("pow : %f\n",pow(n1,n2));    
printf("abs : %d\n",abs(n1));     

}
